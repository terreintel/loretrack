'use strict';

const nodemailer = require('nodemailer');

function createTransporter() {
  return nodemailer.createTransport({
    host:   process.env.SMTP_HOST,
    port:   parseInt(process.env.SMTP_PORT || '587', 10),
    secure: process.env.SMTP_SECURE === 'true',
    auth: {
      user: process.env.SMTP_USER,
      pass: process.env.SMTP_PASS,
    },
  });
}

function buildPlainText(report, transcript) {
  const topics = report.topicsDiscussed?.length
    ? report.topicsDiscussed.map((t) => `  • ${t}`).join('\n')
    : '  None reported';

  const followUp = report.followUpActions?.length
    ? report.followUpActions.map((f) => `  → ${f}`).join('\n')
    : '  None';

  const incidents = report.incidentsOrIssues?.length
    ? report.incidentsOrIssues.map((i) => `  ⚠ ${i}`).join('\n')
    : '  None';

  return [
    'LORETRACK — GBR ENGAGEMENT REPORT',
    '══════════════════════════════════════════',
    `Lead:              ${report.workerName || 'Unknown'}`,
    `Date:              ${report.date || 'Unknown'}`,
    `Region:            ${report.region || '—'}`,
    `TO Group:          ${report.toGroup || '—'}`,
    `Engagement Type:   ${report.engagementType || '—'}`,
    `Attendees:         ${report.attendees || '—'}`,
    '',
    'TOPICS DISCUSSED',
    topics,
    '',
    'CONSENT STATUS',
    `  Status:  ${report.consentStatus || '—'}`,
    ...(report.consentNotes ? [`  Notes:   ${report.consentNotes}`] : []),
    '',
    'FOLLOW-UP ACTIONS',
    followUp,
    ...(report.nextContact ? ['', `NEXT CONTACT:  ${report.nextContact}`] : []),
    ...(report.culturalProtocolNotes ? ['', 'CULTURAL PROTOCOL NOTES', `  ${report.culturalProtocolNotes}`] : []),
    ...(incidents !== '  None' ? ['', 'INCIDENTS / ISSUES', incidents] : []),
    ...(report.additionalNotes ? ['', 'ADDITIONAL NOTES', `  ${report.additionalNotes}`] : []),
    '',
    '──────────────────────────────────────────',
    'ORIGINAL TRANSCRIPT',
    transcript,
    '══════════════════════════════════════════',
    'Generated by LoreTrack — GBR Engagement Reporting | Nirrwara Global',
  ].join('\n');
}

