function esc(str) {
  return String(str ?? '')
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;');
}

async function sendReport(report, supervisorEmail, transcript) {
  const transporter = createTransporter();
  const from = process.env.SMTP_FROM || process.env.SMTP_USER;
  const workerName = report.workerName || 'Regional Engagement Lead';
  const date = report.date || new Date().toLocaleDateString('en-AU');
  const toGroup = report.toGroup ? ` — ${report.toGroup}` : '';

  await transporter.sendMail({
    from,
    to: supervisorEmail,
    subject: `LoreTrack GBR: ${workerName}${toGroup} – ${date}`,
    text: buildPlainText(report, transcript),
    html: buildHtml(report, transcript),
  });
}

module.exports = { sendReport };